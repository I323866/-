<template>
    <div class="login-wrap">
        <div class="ms-title">气象中心信息化管理系统</div>
        <div class="ms-login">
            <el-form :model="ruleForm" :rules="rules" ref="ruleForm" label-width="0px" class="demo-ruleForm">
                <el-form-item prop="username">
                    <el-input v-model="ruleForm.username" placeholder="username"></el-input>
                </el-form-item>
                <el-form-item prop="password">
                    <el-input type="password" placeholder="password" v-model="ruleForm.password" @keyup.enter.native="submitForm('ruleForm')"></el-input>
                </el-form-item>
                <div class="login-btn">
                    <el-button type="primary" @click="submitForm('ruleForm')">登录</el-button>
                </div>
                <p style="font-size:12px;line-height:30px;color:#999;">Tips : 用户名和密码随便填。</p>
            </el-form>
        </div>
    </div>
</template>

<script>
    import {  } from '../../api/';
    export default {
        data: function(){
            return {
                ruleForm: {
                    username: '',
                    password: ''
                },
                rules: {
                    username: [
                        { required: true, message: '请输入用户名', trigger: 'blur' }
                    ],
                    password: [
                        { required: true, message: '请输入密码', trigger: 'blur' }
                    ]
                }
            }
        },
        methods: {
            submitForm(formName) {
                const self = this;
                self.$refs[formName].validate((valid) => {
                    if (valid) {
                        localStorage.setItem('ms_username',self.ruleForm.username);
                        
                    } else {
                        console.log('error submit!!');
                        return false;
                    }
                });
                
                self.$axios.post(`http://localhost:3000/api/login`, {username:self.ruleForm.username,password:self.ruleForm.password}).then((res) => {
                    console.log(res)
                    localStorage.setItem('ms_level',res.data.data.level);
                    self.$router.push('/basetable');
                }).catch(
                    (error)=>{
                        alert("登陆失败");
                    }
                )
            }
        }
    }
</script>

